package br.com.bb.iib;

public class Identificacao {

    private String release;
    private String sistema;
    private String sysplex;
    private String operacao;
    private String servico;
    private String aplicacaoProvedora;
    private String versao;

    /**
     * @return the release
     */
    public String getRelease() {
        return release;
    }

    /**
     * @param release the release to set
     */
    public void setRelease(String release) {
        this.release = release;
    }

    /**
     * @return the sistema
     */
    public String getSistema() {
        return sistema;
    }

    /**
     * @param sistema the sistema to set
     */
    public void setSistema(String sistema) {
        this.sistema = sistema;
    }

    /**
     * @return the sysplex
     */
    public String getSysplex() {
        return sysplex;
    }

    /**
     * @param sysplex the sysplex to set
     */
    public void setSysplex(String sysplex) {
        this.sysplex = sysplex;
    }

    /**
     * @return the operacao
     */
    public String getOperacao() {
        return operacao;
    }

    /**
     * @param operacao the operacao to set
     */
    public void setOperacao(String operacao) {
        this.operacao = operacao;
    }

    /**
     * @return the servico
     */
    public String getServico() {
        return servico;
    }

    /**
     * @param servico the servico to set
     */
    public void setServico(String servico) {
        this.servico = servico;
    }

    /**
     * @return the aplicacaoProvedora
     */
    public String getAplicacaoProvedora() {
        return aplicacaoProvedora;
    }

    /**
     * @param aplicacaoProvedora the aplicacaoProvedora to set
     */
    public void setAplicacaoProvedora(String aplicacaoProvedora) {
        this.aplicacaoProvedora = aplicacaoProvedora;
    }

    /**
     * @return the versao
     */
    public String getVersao() {
        return versao;
    }

    /**
     * @param versao the versao to set
     */
    public void setVersao(String versao) {
        this.versao = versao;
    }

}
